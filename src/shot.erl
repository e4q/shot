-module(shot).

%%% ==================================================================
%%% API
%%% ==================================================================

-export([
  get/1,
  put/1,
  post/1,
  delete/1,
  multipart/1
]).

%%% ==================================================================
%%% Includes
%%% ==================================================================

-include("shot.hrl").

%%% ==================================================================
%%% API functions
%%% ==================================================================

get(URL)        -> shot_utils:http(#{m => get, u => URL}).
put(URL)        -> shot_utils:http(#{m => put, u => URL}).
post(URL)       -> shot_utils:http(#{m => post, u => URL}).
delete(URL)     -> shot_utils:http(#{m => delete, u => URL}).
multipart(Map)  -> shot_utils:multipart(Map).
